def funcao(num):
    if num > 120:
        return Exception
    elif num < 5:
        return Exception
    elif  5 <= num <= 7:
        return 'Infantil A'
    elif  8 <= num <= 10:
        return 'Infantil B'
    elif  11 <= num <= 13:
        return 'Juvenil A'
    elif  14 <= num <= 17:
        return 'Juvenil B'

